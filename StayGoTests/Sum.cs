class Sum
{
    public class HelperClass
    {
        public HelperClass()
        {
        }

        public int Add(int a, int b)
        {
            int x = a + b;
            return x;
        }

        public int Subtract(int a, int b)
        {
            int x = a - b;
            return x;
        }

    }
}